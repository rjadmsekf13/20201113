public class Calculation {
	public int Add(int num1, int num2) {
		return num1 + num2;
	}
	public int Sub(int num1, int num2) {
		return num1 - num2;
	}
	public int Mpy(int num1, int num2) {
		return num1 * num2;
	}
	public int Dvd(int num1, int num2) {
		return num1 / num2;
	}
}
