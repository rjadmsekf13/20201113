public class Calculation {
	public int add(int a,int s) {
		return a + s;
	}
	public int subtract(int a,int s) {
		return a - s;
	}
	public int multiply(int a,int s) {
		return a * s;
	}
	public int divide(int a,int s) {
		return a / s;
	}

}
