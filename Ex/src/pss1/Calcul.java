public class Calcul {
	public int add(int a, int s) {
		return a + s;
	}
	public int stt(int a, int s) {
		return a - s;
	}
	public int mpy(int a, int s) {
		return a * s;
	}
	public int dvd(int a, int s) {
		return a / s;
	}
}
